function erzeugeSeededZufall(seed) {
  let zustand = seed;
  return function () {
    zustand = (zustand * 1103515245 + 12345) & 0x7fffffff;
    return zustand / 0x7fffffff;
  };
}
function zufallInt(zufall, min, max) {
  return Math.floor(zufall() * (max - min + 1)) + min;
}

